function beersong(beercount) {
    beercount = 99;
    while (beercount > 0) {
        return beercount + " bottles of beer on the wall, " + 99 + " bottles of beer. Take one down and pass it around," + beercount - 1 + " bottles of beer on the wall.";
        beercount = beercount - 1;
        
    }

}
beersong(99);
